unsigned short ft_get_4bits_first(unsigned short in)
{
	unsigned short out;

	out = ((in >>= 12) % 2) ? 4096 : 0;
	out += ((in >>= 1) % 2) ? 8192 : 0;
	out += ((in >>= 1) % 2) ? 16384 : 0;
	out += ((in >>= 1) % 2) ? 32768 : 0;
	return (out);
}
